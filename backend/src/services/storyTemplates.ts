export function getRandomTemplate(gender?: 'male' | 'female', genre?: string, emotion?: string): StoryTemplate {
  let filtered = STORY_TEMPLATES;

  if (gender) {
    filtered = filtered.filter(t => t.gender === gender);
  }
  if (genre) {
    filtered = filtered.filter(t => t.genre === genre);
  }
  if (emotion) {
    filtered = filtered.filter(t => t.emotion === emotion);
  }

  if (filtered.length === 0) {
    return STORY_TEMPLATES[Math.floor(Math.random() * STORY_TEMPLATES.length)];
  }

  return filtered[Math.floor(Math.random() * filtered.length)];
}

export function getAllTemplates(): StoryTemplate[] {
  return STORY_TEMPLATES;
}

export function getTemplatesByGenre(genre: string): StoryTemplate[] {
  return STORY_TEMPLATES.filter(t => t.genre === genre);
}

export function getTemplatesByEmotion(emotion: string): StoryTemplate[] {
  return STORY_TEMPLATES.filter(t => t.emotion === emotion);
}

export function getTemplateCount(): number {
  return STORY_TEMPLATES.length;
}
